package com.example.animalfood.Model;

public class BirdModel {
    private int id;
    private String name;
    private String species;
    private String family;
    private String habitat;
    private String place_of_found;
    private String diet;
    private String description;
    private float weight_kg;
    private int wingspan_cm;
    private String image;

    public BirdModel() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSpecies() {
        return species;
    }

    public void setSpecies(String species) {
        this.species = species;
    }

    public String getFamily() {
        return family;
    }

    public void setFamily(String family) {
        this.family = family;
    }

    public String getHabitat() {
        return habitat;
    }

    public void setHabitat(String habitat) {
        this.habitat = habitat;
    }

    public String getPlace_of_found() {
        return place_of_found;
    }

    public void setPlace_of_found(String place_of_found) {
        this.place_of_found = place_of_found;
    }

    public String getDiet() {
        return diet;
    }

    public void setDiet(String diet) {
        this.diet = diet;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public float getWeight_kg() {
        return weight_kg;
    }

    public void setWeight_kg(float weight_kg) {
        this.weight_kg = weight_kg;
    }

    public int getWingspan_cm() {
        return wingspan_cm;
    }

    public void setWingspan_cm(int wingspan_cm) {
        this.wingspan_cm = wingspan_cm;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
